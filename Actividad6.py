
num1 = float(input("Ingresa el primer número: "))
num2 = float(input("Ingresa el segundo número: "))


if num1 > num2:
    print("El número" , num1 ,"es mayor que" , num2)
elif num1 < num2:
    print("El número" , num1, "es menor que", num2)
else:
    print("El número" , num1 , "es igual que", num2)



